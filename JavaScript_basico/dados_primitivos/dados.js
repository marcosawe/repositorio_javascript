/* Tipos de dados primitivos */

// Strings: Comportam textos e caracteres especiais
let nome = 'Marcos Eduardo Fernandes';
let familia = 'Domingues';
let nomeDoGato = "Pascal";

console.log(typeof(nomeDoGato));

// Number: comportam qualquer tipo de numero tanto inteiro quanto float
let numero1 = 12;
let numero2 = 17;
let numero3 = 23.67;

console.log(typeof(numero1));

// Undefined: Comporta variáveis sem apontamento para valores
let valor1;
let valor2;
let valor3;

console.log(valor2);

// Null: comporta valores que são especificados como Nulos e retornam object
const Nulo1 = null
const Nulo2 = null
const Nulo3 = null

console.log(typeof(Nulo2))

// Boolean: Retorna valores verdadeiros e falsos.
const bool1 = true
const bool2 = true
const bool3 = false