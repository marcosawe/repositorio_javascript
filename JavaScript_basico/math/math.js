/* Função Math */
let Num1 = 9.49675;
console.log(Math.floor(Num1)); // Arredonda o valor para a casa decimal de baixo.
console.log(Math.ceil(Num1)); // Arredonda o valor para a casa decimal de cima.
console.log(Math.round(Num1)); //Arredonda o valor para a casa decimal mais próxima.
